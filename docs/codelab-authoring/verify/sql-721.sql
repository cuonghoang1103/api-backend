\echo '===== EX 2: Add Branching and Local Variables with PL/pgSQL ====='
CREATE OR REPLACE FUNCTION stock_status(p_id INT) RETURNS TEXT LANGUAGE plpgsql AS $$
DECLARE v_stock INT;
BEGIN
  SELECT stock INTO v_stock FROM products WHERE id = p_id;
  IF NOT FOUND THEN RETURN 'unknown';
  ELSIF v_stock = 0 THEN RETURN 'out';
  ELSIF v_stock <= 2 THEN RETURN 'low';
  ELSIF v_stock <= 9 THEN RETURN 'ok';
  ELSE RETURN 'plenty';
  END IF;
END; $$;
SELECT name, stock, stock_status(id) AS status FROM products ORDER BY name;
SELECT stock_status(99) AS missing_product;

\echo '===== EX 3: Return a Result Set with RETURNS TABLE ====='
CREATE OR REPLACE FUNCTION products_below(p_max NUMERIC) RETURNS TABLE(name TEXT, price NUMERIC, status TEXT) LANGUAGE plpgsql STABLE AS $$
BEGIN
  IF p_max < 0 THEN RETURN; END IF;
  RETURN QUERY SELECT p.name, p.price, stock_status(p.id) FROM products p WHERE p.price <= p_max ORDER BY p.price DESC;
END; $$;
SELECT * FROM products_below(50);
SELECT * FROM products_below(50) WHERE status <> 'out';
SELECT count(*) AS rows_for_negative FROM products_below(-1);

\echo '===== EX 4: Use a PROCEDURE to Control Transactions ====='
CREATE OR REPLACE PROCEDURE restock_all(p_amount INT, INOUT p_touched INT DEFAULT 0) LANGUAGE plpgsql AS $$
DECLARE rec RECORD;
BEGIN
  p_touched := 0;
  FOR rec IN SELECT id FROM products ORDER BY id LOOP
    UPDATE products SET stock = stock + p_amount WHERE id = rec.id;
    p_touched := p_touched + 1;
    IF p_touched % 2 = 0 THEN COMMIT; END IF;
  END LOOP;
  COMMIT;
END; $$;
CALL restock_all(5, 0);
SELECT name, stock FROM products ORDER BY name;
