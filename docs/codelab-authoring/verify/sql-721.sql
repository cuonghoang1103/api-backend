\echo '===== EX 5: Handle Errors with EXCEPTION Blocks and RAISE ====='
CREATE OR REPLACE FUNCTION add_customer(p_id INT, p_name TEXT, p_email TEXT) RETURNS TEXT LANGUAGE plpgsql AS $$
BEGIN
  IF position('@' IN p_email) = 0 THEN RAISE EXCEPTION 'invalid email' USING ERRCODE = '22000'; END IF;
  INSERT INTO customers (id, name, email) VALUES (p_id, p_name, p_email);
  RETURN 'created';
EXCEPTION
  WHEN unique_violation THEN RETURN 'duplicate';
  WHEN OTHERS THEN RETURN 'error:' || SQLSTATE;
END; $$;
SELECT add_customer(4, 'Di', 'di@x.io') AS first_insert;
SELECT add_customer(4, 'Dup', 'other@x.io') AS same_id;
SELECT add_customer(5, 'Ann2', 'ann@x.io') AS same_email;
SELECT add_customer(6, 'Bad', 'nope') AS bad_email;
SELECT count(*) AS customers FROM customers;

\echo '===== EX 6: Record an Audit Trail with an AFTER Trigger ====='
CREATE OR REPLACE FUNCTION log_product_change() RETURNS TRIGGER LANGUAGE plpgsql AS $$
BEGIN
  IF TG_OP = 'DELETE' THEN INSERT INTO audit_log (action, entity, entity_id) VALUES (lower(TG_OP), 'product', OLD.id);
  ELSE INSERT INTO audit_log (action, entity, entity_id) VALUES (lower(TG_OP), 'product', NEW.id);
  END IF;
  RETURN NULL;
END; $$;
CREATE TRIGGER trg_product_audit AFTER INSERT OR UPDATE OR DELETE ON products FOR EACH ROW EXECUTE FUNCTION log_product_change();
