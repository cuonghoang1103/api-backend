\echo '===== EX 1: Write a Reusable Scalar Function in Plain SQL ====='
CREATE OR REPLACE FUNCTION discounted(price NUMERIC, pct NUMERIC) RETURNS NUMERIC LANGUAGE sql IMMUTABLE STRICT AS $$ SELECT ROUND(price - price * pct / 100, 2); $$;
SELECT name, price, discounted(price, 10) AS sale_price FROM products ORDER BY name;
