SELECT action, entity, entity_id FROM audit_log ORDER BY id;
SELECT count(*) AS audit_rows FROM audit_log;
DROP TRIGGER trg_product_audit ON products;

\echo '===== EX 7: Normalise Data with a BEFORE Trigger ====='
CREATE OR REPLACE FUNCTION tidy_customer() RETURNS TRIGGER LANGUAGE plpgsql AS $$
BEGIN
  NEW.name := btrim(NEW.name);
  NEW.email := lower(btrim(NEW.email));
  IF NEW.name = '' THEN RETURN NULL; END IF;
  RETURN NEW;
END; $$;
