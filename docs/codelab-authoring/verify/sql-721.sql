\echo '===== EX 8: Build Dynamic SQL Safely with EXECUTE and format ====='
CREATE OR REPLACE FUNCTION row_count(p_table TEXT) RETURNS BIGINT LANGUAGE plpgsql AS $$
DECLARE v_count BIGINT;
BEGIN EXECUTE format('SELECT count(*) FROM %I', p_table) INTO v_count; RETURN v_count; END; $$;
CREATE OR REPLACE FUNCTION find_by(p_table TEXT, p_column TEXT, p_value TEXT) RETURNS BIGINT LANGUAGE plpgsql AS $$
DECLARE v_count BIGINT;
BEGIN EXECUTE format('SELECT count(*) FROM %I WHERE %I = $1', p_table, p_column) INTO v_count USING p_value; RETURN v_count; END; $$;
SELECT row_count('products') AS products;
SELECT row_count('customers') AS customers;
SELECT find_by('customers', 'name', 'Ann') AS found_ann;
SELECT find_by('customers', 'name', ''' OR 1=1 --') AS injected_value;
DO $$ DECLARE v BIGINT; BEGIN v := row_count('products; DROP TABLE customers; --'); RAISE NOTICE 'unexpected %', v; EXCEPTION WHEN OTHERS THEN RAISE NOTICE 'injected identifier failed with SQLSTATE %', SQLSTATE; END; $$;
SELECT count(*) AS customers_still_there FROM customers;

\echo '===== EX 9: Replace a Row-by-Row Loop with One Set-Based Statement ====='
CREATE OR REPLACE FUNCTION markup_loop(p_pct NUMERIC) RETURNS INT LANGUAGE plpgsql AS $$
DECLARE rec RECORD; v_rows INT := 0;
BEGIN
  FOR rec IN SELECT id, price FROM products ORDER BY id LOOP
    UPDATE products SET price = ROUND(rec.price * (1 + p_pct / 100), 2) WHERE id = rec.id;
    v_rows := v_rows + 1;
  END LOOP;
  RETURN v_rows;
END; $$;
CREATE OR REPLACE FUNCTION markup_set(p_pct NUMERIC) RETURNS INT LANGUAGE plpgsql AS $$
DECLARE v_rows INT;
BEGIN UPDATE products SET price = ROUND(price * (1 + p_pct / 100), 2); GET DIAGNOSTICS v_rows = ROW_COUNT; RETURN v_rows; END; $$;
SELECT markup_loop(10) AS loop_rows;
SELECT name, price FROM products ORDER BY name;
SELECT markup_set(-10) AS set_rows;
SELECT name, price FROM products ORDER BY name;
SELECT (SELECT markup_loop(0)) = (SELECT markup_set(0)) AS same_rows;

\echo '===== EX 10: Capstone: An Order-Placement Procedure That Cannot Oversell ====='
CREATE OR REPLACE PROCEDURE place_order(p_customer INT, p_product INT, p_qty INT, INOUT p_result TEXT DEFAULT '') LANGUAGE plpgsql AS $$
DECLARE v_price NUMERIC; v_rows INT; v_order INT; v_dummy INT;
BEGIN
  IF p_qty IS NULL OR p_qty <= 0 THEN RAISE EXCEPTION 'quantity must be positive' USING ERRCODE = '22023'; END IF;
  SELECT id INTO v_dummy FROM customers WHERE id = p_customer;
  IF NOT FOUND THEN p_result := 'no-customer'; RETURN; END IF;
  SELECT price INTO v_price FROM products WHERE id = p_product;
  IF NOT FOUND THEN p_result := 'no-product'; RETURN; END IF;
  UPDATE products SET stock = stock - p_qty WHERE id = p_product AND stock >= p_qty;
  GET DIAGNOSTICS v_rows = ROW_COUNT;
  IF v_rows = 0 THEN p_result := 'insufficient-stock'; RETURN; END IF;
  INSERT INTO orders (customer_id, total, status) VALUES (p_customer, v_price * p_qty, 'placed') RETURNING id INTO v_order;
  INSERT INTO order_items (order_id, product_id, qty) VALUES (v_order, p_product, p_qty);
  INSERT INTO audit_log (action, entity, entity_id) VALUES ('order', 'order', v_order);
  p_result := 'ok:' || v_order;
EXCEPTION WHEN OTHERS THEN p_result := 'error:' || SQLSTATE;
END; $$;
CALL place_order(1, 1, 2);
