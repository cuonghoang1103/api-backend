'use client';

import { useState, useEffect, useCallback } from 'react';
import { toast } from 'sonner';
import {
  Search,
  Send,
  Globe,
  CheckCircle2,
  XCircle,
  AlertCircle,
  Loader2,
  ExternalLink,
  RefreshCw,
  FileText,
  Clock,
  Zap,
} from 'lucide-react';

interface SitemapUrl {
  url: string;
  lastmod?: string;
  changefreq?: string;
  priority?: number;
}

interface IndexResult {
  url: string;
  status: 'pending' | 'success' | 'error' | 'throttled';
  httpCode?: number;
  message?: string;
}

/**
 * /admin/seo — manual Google Indexing API tool
 *
 * Background:
 *   Google deprecated the legacy /ping?sitemap= endpoint in 2023.
 *   The active way to tell Google "please crawl this URL now" is
 *   the Indexing API, which is what /api/index-url wraps. The
 *   auto-ping in deploy-vps.sh handles *new* URLs in the sitemap,
 *   but admins sometimes need to:
 *     - re-ping a URL that was changed retroactively (not new)
 *     - push a URL right after publishing, without waiting for
 *       the next deploy
 *     - audit which URLs Google knows about
 *
 *   This page exposes that capability with a UI.
 *
 * Throttling note:
 *   The route enforces 60s between requests for the same URL.
 *   We surface that here so the user understands why re-clicking
 *   "Submit" for the same URL returns a 429.
 */
export default function AdminSeoPage() {
  const [sitemap, setSitemap] = useState<SitemapUrl[]>([]);
  const [loadingSitemap, setLoadingSitemap] = useState(true);
  const [customUrl, setCustomUrl] = useState('');
  const [submitting, setSubmitting] = useState(false);
  const [results, setResults] = useState<Record<string, IndexResult>>({});
  const [filter, setFilter] = useState('');

  const fetchSitemap = useCallback(async () => {
    setLoadingSitemap(true);
    try {
      const res = await fetch('/sitemap.xml', { cache: 'no-store' });
      if (!res.ok) throw new Error(`HTTP ${res.status}`);
      const xml = await res.text();
      // Lightweight XML parse — no DOMParser in all envs, so
      // we use regex. The sitemap is small (<200 URLs) and
      // generated by our own Next.js sitemap.ts, so the format
      // is stable.
      const urls: SitemapUrl[] = [];
      const re = /<url>([\s\S]*?)<\/url>/g;
      let m: RegExpExecArray | null;
      while ((m = re.exec(xml)) !== null) {
        const block = m[1];
        const loc = block.match(/<loc>([^<]+)<\/loc>/);
        if (!loc) continue;
        const lastmod = block.match(/<lastmod>([^<]+)<\/lastmod>/)?.[1];
        const changefreq = block.match(/<changefreq>([^<]+)<\/changefreq>/)?.[1];
        const pri = block.match(/<priority>([^<]+)<\/priority>/)?.[1];
        urls.push({
          url: loc[1],
          lastmod,
          changefreq,
          priority: pri ? parseFloat(pri) : undefined,
        });
      }
      setSitemap(urls);
    } catch (err: any) {
      toast.error('Không tải được sitemap.xml');
      setSitemap([]);
    } finally {
      setLoadingSitemap(false);
    }
  }, []);

  useEffect(() => { fetchSitemap(); }, [fetchSitemap]);

  const submit = useCallback(async (url: string) => {
    // Optimistic UI: mark as pending first
    setResults((prev) => ({
      ...prev,
      [url]: { url, status: 'pending' },
    }));
    try {
      const res = await fetch('/api/index-url', {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({ url }),
      });
      const data = await res.json().catch(() => ({}));
      const httpCode = res.status;

      if (res.ok && data.success) {
        setResults((prev) => ({
          ...prev,
          [url]: { url, status: 'success', httpCode, message: 'Indexed' },
        }));
        toast.success(`Đã submit: ${url}`);
      } else if (httpCode === 429) {
        setResults((prev) => ({
          ...prev,
          [url]: { url, status: 'throttled', httpCode, message: data.error || 'Throttled' },
        }));
        toast.warning(`Throttled (đợi 60s): ${url}`);
      } else {
        const msg = data.error || `HTTP ${httpCode}`;
        setResults((prev) => ({
          ...prev,
          [url]: { url, status: 'error', httpCode, message: msg },
        }));
        toast.error(`Lỗi: ${url} — ${msg}`);
      }
    } catch (err: any) {
      setResults((prev) => ({
        ...prev,
        [url]: { url, status: 'error', message: err?.message || 'Network error' },
      }));
      toast.error(`Network error: ${url}`);
    }
  }, []);

  const handleCustomSubmit = async () => {
    const url = customUrl.trim();
    if (!url) {
      toast.error('Nhập URL trước');
      return;
    }
    if (!/^https?:\/\//i.test(url)) {
      toast.error('URL phải bắt đầu bằng http(s)://');
      return;
    }
    setSubmitting(true);
    await submit(url);
    setSubmitting(false);
  };

  const handleSitemapSubmit = async (url: string) => {
    setSubmitting(true);
    await submit(url);
    setSubmitting(false);
  };

  const handleSubmitAll = async () => {
    if (filtered.length === 0) {
      toast.warning('Không có URL nào để submit');
      return;
    }
    if (!confirm(`Submit ${filtered.length} URLs? Throttle 65s/URL, có thể mất ${Math.ceil(filtered.length * 65 / 60)} phút.`)) {
      return;
    }
    setSubmitting(true);
    for (const u of filtered) {
      await submit(u.url);
      // 65s between requests — let user know we're working
      if (u !== filtered[filtered.length - 1]) {
        await new Promise((r) => setTimeout(r, 65000));
      }
    }
    setSubmitting(false);
    toast.success('Batch hoàn tất');
  };

  // Stats
  const stats = {
    total: sitemap.length,
    success: Object.values(results).filter((r) => r.status === 'success').length,
    error: Object.values(results).filter((r) => r.status === 'error').length,
    throttled: Object.values(results).filter((r) => r.status === 'throttled').length,
    pending: Object.values(results).filter((r) => r.status === 'pending').length,
  };

  // Filter
  const filtered = filter
    ? sitemap.filter((u) => u.url.toLowerCase().includes(filter.toLowerCase()))
    : sitemap;

  const formatDate = (s?: string) => {
    if (!s) return '—';
    try {
      return new Date(s).toLocaleString('vi-VN', {
        day: '2-digit', month: '2-digit', year: 'numeric',
        hour: '2-digit', minute: '2-digit',
      });
    } catch { return s; }
  };

  const statusBadge = (r?: IndexResult) => {
    if (!r) {
      return <span className="text-xs text-text-muted">chưa submit</span>;
    }
    switch (r.status) {
      case 'pending':
        return (
          <span className="inline-flex items-center gap-1 text-xs text-neon-cyan">
            <Loader2 className="w-3 h-3 animate-spin" /> đang gửi
          </span>
        );
      case 'success':
        return (
          <span className="inline-flex items-center gap-1 text-xs text-emerald-400">
            <CheckCircle2 className="w-3 h-3" /> {r.httpCode} OK
          </span>
        );
      case 'throttled':
        return (
          <span className="inline-flex items-center gap-1 text-xs text-yellow-400">
            <Clock className="w-3 h-3" /> 429
          </span>
        );
      case 'error':
        return (
          <span className="inline-flex items-center gap-1 text-xs text-red-400" title={r.message}>
            <XCircle className="w-3 h-3" /> {r.httpCode || 'ERR'}
          </span>
        );
    }
  };

  return (
    <div className="space-y-6">
      {/* Header */}
      <div className="flex items-center justify-between flex-wrap gap-3">
        <div>
          <h1 className="text-2xl font-heading font-bold text-text-primary">SEO Tools</h1>
          <p className="text-text-secondary mt-1">Submit URLs lên Google Indexing API thủ công</p>
        </div>
        <button
          onClick={fetchSitemap}
          disabled={loadingSitemap}
          className="flex items-center gap-2 px-4 py-2.5 bg-darkcard border border-darkborder rounded-xl text-sm text-text-secondary hover:text-text-primary hover:border-neon-violet/30 transition-colors disabled:opacity-50"
        >
          <RefreshCw className={`w-4 h-4 ${loadingSitemap ? 'animate-spin' : ''}`} />
          Tải lại sitemap
        </button>
      </div>

      {/* Info banner */}
      <div className="bg-neon-violet/10 border border-neon-violet/20 rounded-2xl p-4 flex items-start gap-3">
        <AlertCircle className="w-5 h-5 text-neon-violet flex-shrink-0 mt-0.5" />
        <div className="text-sm text-text-secondary space-y-1">
          <p>
            <strong className="text-text-primary">Google Indexing API</strong> —
            yêu cầu Google crawl URL trong vài phút (thay vì vài ngày như sitemap-only).
            Quota: <strong className="text-text-primary">200 requests/ngày/project</strong>.
          </p>
          <p>
            Auto-ping trong deploy-vps.sh đã lo hầu hết trường hợp (URL mới trong sitemap).
            Trang này dùng cho <strong className="text-text-primary">re-ping</strong> URL đã thay đổi nội dung,
            hoặc submit gấp không muốn đợi deploy.
          </p>
        </div>
      </div>

      {/* Stats */}
      <div className="grid grid-cols-2 sm:grid-cols-5 gap-3">
        <StatCard label="Tổng URLs" value={stats.total} icon={Globe} color="from-neon-indigo to-neon-violet" />
        <StatCard label="Success" value={stats.success} icon={CheckCircle2} color="from-emerald-500 to-emerald-400" />
        <StatCard label="Throttled" value={stats.throttled} icon={Clock} color="from-yellow-500 to-yellow-400" />
        <StatCard label="Error" value={stats.error} icon={XCircle} color="from-red-500 to-red-400" />
        <StatCard label="Pending" value={stats.pending} icon={Loader2} color="from-neon-cyan to-neon-blue" />
      </div>

      {/* Custom URL submit */}
      <div className="bg-darkcard border border-darkborder rounded-2xl p-5">
        <h2 className="font-heading font-bold text-text-primary mb-3 flex items-center gap-2">
          <Send className="w-5 h-5 text-neon-violet" />
          Submit URL tuỳ ý
        </h2>
        <div className="flex flex-col sm:flex-row gap-3">
          <div className="relative flex-1">
            <Globe className="absolute left-3 top-1/2 -translate-y-1/2 w-4 h-4 text-text-muted" />
            <input
              type="url"
              value={customUrl}
              onChange={(e) => setCustomUrl(e.target.value)}
              onKeyDown={(e) => { if (e.key === 'Enter') handleCustomSubmit(); }}
              placeholder="https://cuongthai.com/blog/abc"
              className="w-full pl-10 pr-4 py-2.5 bg-darkbg border border-darkborder rounded-xl text-sm text-text-primary placeholder:text-text-muted focus:outline-none focus:border-neon-violet/50 transition-colors"
            />
          </div>
          <button
            onClick={handleCustomSubmit}
            disabled={submitting || !customUrl.trim()}
            className="flex items-center justify-center gap-2 px-5 py-2.5 bg-gradient-to-r from-neon-indigo to-neon-violet text-white text-sm font-medium rounded-xl hover:opacity-90 transition-opacity disabled:opacity-50 disabled:cursor-not-allowed"
          >
            {submitting ? <Loader2 className="w-4 h-4 animate-spin" /> : <Send className="w-4 h-4" />}
            Submit
          </button>
        </div>
        <p className="text-xs text-text-muted mt-2">
          ⚠️ Chỉ submit URL thuộc domain <code className="text-text-secondary">cuongthai.com</code>.
          URL ngoài domain sẽ bị route từ chối với lý do bảo mật.
        </p>
      </div>

      {/* Sitemap URLs */}
      <div className="bg-darkcard border border-darkborder rounded-2xl overflow-hidden">
        <div className="px-5 py-4 border-b border-darkborder flex items-center justify-between flex-wrap gap-3">
          <h2 className="font-heading font-bold text-text-primary flex items-center gap-2">
            <FileText className="w-5 h-5 text-neon-violet" />
            URLs trong sitemap ({filtered.length}/{sitemap.length})
          </h2>
          <div className="flex items-center gap-2">
            <div className="relative">
              <Search className="absolute left-3 top-1/2 -translate-y-1/2 w-3.5 h-3.5 text-text-muted" />
              <input
                type="text"
                value={filter}
                onChange={(e) => setFilter(e.target.value)}
                placeholder="Lọc URL..."
                className="pl-9 pr-3 py-1.5 bg-darkbg border border-darkborder rounded-lg text-xs text-text-primary placeholder:text-text-muted focus:outline-none focus:border-neon-violet/50 w-40"
              />
            </div>
            <button
              onClick={handleSubmitAll}
              disabled={submitting || filtered.length === 0}
              className="flex items-center gap-1.5 px-3 py-1.5 bg-darkbg border border-darkborder rounded-lg text-xs text-text-secondary hover:text-text-primary hover:border-neon-violet/30 transition-colors disabled:opacity-50"
              title="Submit tất cả URLs đã lọc"
            >
              <Zap className="w-3.5 h-3.5" />
              Submit all
            </button>
          </div>
        </div>

        <div className="overflow-x-auto">
          <table className="w-full">
            <thead>
              <tr className="border-b border-darkborder">
                <th className="text-left px-5 py-3 text-xs font-medium text-text-muted uppercase tracking-wider">URL</th>
                <th className="text-left px-5 py-3 text-xs font-medium text-text-muted uppercase tracking-wider hidden md:table-cell">Lastmod</th>
                <th className="text-left px-5 py-3 text-xs font-medium text-text-muted uppercase tracking-wider hidden lg:table-cell">Priority</th>
                <th className="text-left px-5 py-3 text-xs font-medium text-text-muted uppercase tracking-wider">Status</th>
                <th className="text-right px-5 py-3 text-xs font-medium text-text-muted uppercase tracking-wider">Thao tác</th>
              </tr>
            </thead>
            <tbody className="divide-y divide-darkborder">
              {loadingSitemap ? (
                [...Array(5)].map((_, i) => (
                  <tr key={i}>
                    <td colSpan={5} className="px-5 py-4">
                      <div className="h-4 bg-darkborder/50 rounded animate-pulse" />
                    </td>
                  </tr>
                ))
              ) : filtered.length === 0 ? (
                <tr>
                  <td colSpan={5} className="text-center py-12 text-text-muted">
                    <Globe className="w-12 h-12 mx-auto mb-3 opacity-30" />
                    <p>Không có URL nào</p>
                  </td>
                </tr>
              ) : (
                filtered.map((u) => {
                  const r = results[u.url];
                  return (
                    <tr key={u.url} className="hover:bg-white/[0.02] transition-colors">
                      <td className="px-5 py-3">
                        <div className="flex items-center gap-2 min-w-0">
                          <a
                            href={u.url}
                            target="_blank"
                            rel="noopener noreferrer"
                            className="text-sm text-text-primary hover:text-neon-violet transition-colors truncate flex items-center gap-1"
                          >
                            <span className="truncate max-w-md">{u.url}</span>
                            <ExternalLink className="w-3 h-3 flex-shrink-0" />
                          </a>
                        </div>
                      </td>
                      <td className="px-5 py-3 hidden md:table-cell">
                        <span className="text-xs text-text-muted">{formatDate(u.lastmod)}</span>
                      </td>
                      <td className="px-5 py-3 hidden lg:table-cell">
                        <span className="text-xs text-text-secondary font-mono">
                          {u.priority?.toFixed(1) ?? '—'}
                        </span>
                      </td>
                      <td className="px-5 py-3">{statusBadge(r)}</td>
                      <td className="px-5 py-3 text-right">
                        <button
                          onClick={() => handleSitemapSubmit(u.url)}
                          disabled={submitting || r?.status === 'pending'}
                          className="inline-flex items-center gap-1 px-3 py-1.5 bg-neon-violet/10 text-neon-violet rounded-lg text-xs font-medium hover:bg-neon-violet/20 transition-colors disabled:opacity-50 disabled:cursor-not-allowed"
                        >
                          {r?.status === 'pending' ? (
                            <Loader2 className="w-3 h-3 animate-spin" />
                          ) : (
                            <Send className="w-3 h-3" />
                          )}
                          Submit
                        </button>
                      </td>
                    </tr>
                  );
                })
              )}
            </tbody>
          </table>
        </div>
      </div>

      {/* Help footer */}
      <div className="text-xs text-text-muted space-y-1">
        <p>
          💡 Mẹo: Throttle 60s/URL được route enforce. Nếu submit 1 URL 2 lần trong 60s, lần 2 sẽ trả 429.
        </p>
        <p>
          💡 Auto-ping trong deploy-vps.sh tự động chạy sau mỗi deploy. Submit thủ công ở đây chỉ cần thiết
          khi URL đã tồn tại trong sitemap nhưng nội dung đã thay đổi đáng kể, hoặc cần index gấp.
        </p>
      </div>
    </div>
  );
}

function StatCard({ label, value, icon: Icon, color }: { label: string; value: number; icon: React.ElementType; color: string }) {
  return (
    <div className="bg-darkcard border border-darkborder rounded-2xl p-4">
      <div className={`w-9 h-9 rounded-lg bg-gradient-to-br ${color} flex items-center justify-center mb-2`}>
        <Icon className="w-4 h-4 text-white" />
      </div>
      <p className="text-2xl font-heading font-bold text-text-primary">{value}</p>
      <p className="text-xs text-text-muted mt-0.5">{label}</p>
    </div>
  );
}
