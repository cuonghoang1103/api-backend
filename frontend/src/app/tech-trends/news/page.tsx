import type { Metadata } from 'next';
import Link from 'next/link';
import { getServerApiBaseUrl } from '@/lib/server-api';
import type { Article, NewsSource } from '../types';

/**
 * /tech-trends/news — the bulletin feed.
 *
 * Rendered on the server so the day's headlines are crawlable and readable
 * without JavaScript; a news page that needs a hydration pass before showing
 * any text is the wrong trade for this kind of content.
 *
 * Bulletins are strictly chronological — no "featured" pinning — because a
 * pinned bulletin from last week reads as today's news.
 */

export const dynamic = 'force-dynamic';

export const metadata: Metadata = {
  title: 'Bảng tin công nghệ | cuongthai.com',
  description:
    'Bản tin công nghệ hằng ngày: tổng hợp thông báo chính thức từ GitHub, Node.js, React, OpenAI, AWS, Cloudflare và các nguồn khác, kèm link tới bài gốc.',
  alternates: { canonical: '/tech-trends/news' },
  openGraph: {
    title: 'Bảng tin công nghệ',
    description: 'Tổng hợp thông báo chính thức trong ngày, luôn kèm nguồn.',
    type: 'website',
    url: '/tech-trends/news',
  },
};

async function getBulletins(): Promise<Article[]> {
  try {
    const res = await fetch(`${getServerApiBaseUrl()}/tech-trends/articles?kind=NEWS&size=30`, {
      next: { revalidate: 120 },
    });
    if (!res.ok) return [];
    const json = (await res.json()) as { data?: Article[] };
    return json.data ?? [];
  } catch {
    // A feed that fails to load should render an empty state, not a 500.
    return [];
  }
}

function formatDateTime(iso: string | null | undefined): string {
  if (!iso) return '';
  return new Intl.DateTimeFormat('vi-VN', {
    day: '2-digit', month: '2-digit', year: 'numeric',
    hour: '2-digit', minute: '2-digit', timeZone: 'Asia/Ho_Chi_Minh',
  }).format(new Date(iso));
}

function publishersOf(sources: NewsSource[] | undefined): string[] {
  return [...new Set((sources ?? []).map((s) => s.publisher))].slice(0, 6);
}

export default async function NewsPage() {
  const bulletins = await getBulletins();
  const [latest, ...rest] = bulletins;

  return (
    <main className="min-h-screen px-4 py-10 sm:py-14">
      <div className="max-w-5xl mx-auto">
        {/* Masthead */}
        <header className="border-b-4 border-double border-darkborder pb-6 mb-8">
          <div className="flex flex-wrap items-end justify-between gap-4">
            <div>
              <p className="text-xs tracking-[0.35em] text-neon-violet font-semibold">
                CUONGTHAI.COM
              </p>
              <h1 className="mt-1 font-heading text-3xl sm:text-5xl font-bold text-text-primary">
                Bảng tin công nghệ
              </h1>
              <p className="mt-2 text-sm text-text-secondary max-w-2xl">
                Tổng hợp thông báo chính thức trong ngày từ blog kỹ thuật của các hãng. Mỗi tin đều
                dẫn thẳng tới bài gốc để bạn tự kiểm chứng.
              </p>
            </div>
            <Link
              href="/tech-trends"
              className="text-sm text-neon-violet hover:underline shrink-0"
            >
              ← Bài viết chuyên sâu
            </Link>
          </div>
        </header>

        {bulletins.length === 0 ? (
          <div className="py-20 text-center">
            <p className="text-5xl mb-3">📰</p>
            <p className="text-text-primary font-semibold">Chưa có bản tin nào</p>
            <p className="text-sm text-text-secondary mt-1">
              Bản tin mới được đăng tự động hằng ngày lúc 07:30.
            </p>
          </div>
        ) : (
          <>
            {/* Lead story */}
            <article className="mb-12">
              <Link href={`/tech-trends/${latest.slug}`} className="group block">
                {latest.coverImageUrl && (
                  // Bulletin covers are generated by us at a fixed 1200×630, so a
                  // plain img with an explicit ratio avoids any layout shift.
                  // eslint-disable-next-line @next/next/no-img-element
                  <img
                    src={latest.coverImageUrl}
                    alt=""
                    width={1200}
                    height={630}
                    className="w-full rounded-2xl border border-darkborder mb-5 aspect-[1200/630] object-cover"
                  />
                )}
                <p className="text-xs uppercase tracking-widest text-neon-violet font-semibold">
                  Mới nhất · {formatDateTime(latest.publishedAt)}
                </p>
                <h2 className="mt-2 font-heading text-2xl sm:text-4xl font-bold text-text-primary group-hover:text-neon-violet transition-colors">
                  {latest.title}
                </h2>
                <p className="mt-3 text-base text-text-secondary leading-relaxed">
                  {latest.summary}
                </p>
              </Link>
              <div className="mt-4 flex flex-wrap items-center gap-2 text-xs">
                {latest.aiGenerated && (
                  <span className="px-2 py-1 rounded-full bg-neon-violet/10 text-neon-violet border border-neon-violet/20">
                    AI tổng hợp · đã dẫn nguồn
                  </span>
                )}
                {publishersOf(latest.sources).map((p) => (
                  <span key={p} className="px-2 py-1 rounded-full bg-darkcard text-text-secondary border border-darkborder">
                    {p}
                  </span>
                ))}
              </div>
            </article>

            {/* Archive */}
            {rest.length > 0 && (
              <section>
                <h3 className="text-xs uppercase tracking-widest text-text-secondary font-semibold border-t border-darkborder pt-6 mb-4">
                  Các bản tin trước
                </h3>
                <ul className="divide-y divide-darkborder">
                  {rest.map((a) => (
                    <li key={a.id} className="py-5">
                      <Link href={`/tech-trends/${a.slug}`} className="group flex gap-4">
                        {a.coverImageUrl && (
                          // eslint-disable-next-line @next/next/no-img-element
                          <img
                            src={a.coverImageUrl}
                            alt=""
                            width={200}
                            height={105}
                            className="hidden sm:block w-40 shrink-0 rounded-lg border border-darkborder aspect-[1200/630] object-cover"
                          />
                        )}
                        <div className="min-w-0">
                          <p className="text-xs text-text-secondary">
                            {formatDateTime(a.publishedAt)}
                            {a.sources && a.sources.length > 0 && (
                              <span> · {a.sources.length} nguồn</span>
                            )}
                          </p>
                          <h4 className="mt-1 font-heading text-lg font-semibold text-text-primary group-hover:text-neon-violet transition-colors">
                            {a.title}
                          </h4>
                          <p className="mt-1 text-sm text-text-secondary line-clamp-2">{a.summary}</p>
                        </div>
                      </Link>
                    </li>
                  ))}
                </ul>
              </section>
            )}
          </>
        )}
      </div>
    </main>
  );
}
